const slopes = [
    [1, 1],
    [3, 1],
    [5, 1],
    [7, 1],
    [1, 2],
];

function parseInput(inputText) {
    return inputText.split('\n');
}

function resolve(input) {
    const rows = input.length;
    const rowLength = input[0].length;
    const tree = '#';

    const trees = slopes.map(([x, y]) => {
        let coords = {
            'x': 0,
            'y': 0
        };
        let count = 0;
        while (coords.y < rows) {
            if (input[coords.y][coords.x] === tree) {
                count++;
            }
            coords.x = (coords.x + x) % rowLength;
            coords.y += y;
        }
        return count
    });

    let output = trees.reduce((prevValue, curValue) => {
        return prevValue * curValue
    }, 1);

    return output;

}



